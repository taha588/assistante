const defaultLang = 'fr';

export const getTranslator = (lang: string) => {
    const langKey = translations[lang] ? lang : defaultLang;
    const translationSet = translations[langKey];
    const defaultSet = translations[defaultLang];

    return (key: string, params?: any): string => {
        const value = translationSet[key] || defaultSet[key];

        if (typeof value === 'function') {
            return value(params);
        }

        return value || key;
    };
};